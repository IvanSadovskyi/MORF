                                <h2><strong>Navy Blue</strong></h2>
                                <h3>By using Navy Blue, The Griffith Observatory brand
                                can visually reference the nighttime atmosphere and
                                the beauty of the cosmos.</h3>
                                <p>Navy Blue color establish a link between 
                                the brand's visual identity and its purpose  of astronomical exploration. Furthermore, navy blue can help create a sense of depth and mystery, reminiscent of the vast expanse of space. It can evoke a feeling of awe and wonder that one might experience when looking up at a starry night sky.</p>
                            </div>
