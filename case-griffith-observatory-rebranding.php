                            <div class="work-texts__wrap"><h2><strong>work-texts</strong></h2></div>
                            <div class="work-texts__wrap">
                                <p>This case study showcases the rebranding of the Griffith Observatory, blending Minimalism with Art Deco influences. The goal was to craft a modern, elegant identity that honors the observatory’s iconic legacy while enhancing its visual presence. By streamlining design elements and infusing Art Deco’s timeless sophistication, the rebrand creates a cohesive and memorable experience across all touchpoints.</p>
                            </div>
                        </div>
                    </div>
                    <section class="work-texts container">
                        <div class="row row-g96 row-nw">
                            <div class="col-1-2 embed-responsive embed-responsive--1-1" style="margin-bottom: 0;">
                                <img src="/assets/img/cases/griffith-observatory-rebranding/griffith-4.png" alt="">
                            </div>
                            <div class="col-1-2 section-texts" style="margin-bottom: 0; display: flex; justify-content: center; flex-direction: column;">
                                <h2><strong>New Logo</strong></h2>
                                <p>The logo features a distinct outline of The Griffith Observatory's
                                    main dome on the left side. The outline captures the iconic shape
                                    of the dome, showcasing its recognizable architecture. It consist
                                    of a clean and simplified outline that retains the essential structure of the dome, emphasizing its unique silhouette.</p>
                            </div>
                        </div>
                    </section>
                    <div class="work-texts container">
                        
                        <div class="row row-g96 row-nw">
                            <div class="col-1-2" style="margin-bottom: 0;">
                                <h3 style="margin-bottom: 16px;"><strong>Aesthetic</strong></h3>
                                <p>The new minimalistic and modern logo for The Griffith Observatory embodies simplicity, elegance, and contemporary design principles. By adopting a  minimalistic approach, the logo aims to convey a sense of sophistication while maintaining a clean and uncluttered aesthetic.</p>
                            </div>
                            <div class="col-1-2" style="margin-bottom: 0;">
                                <h3 style="margin-bottom: 16px;"><strong>Definite Article</strong></h3>
                                <p>The use of "The" can convey that the Griffith Observatory has a long-standing reputation and is widely recognized within its field. It implies that the observatory holds a significant place in history or is an iconic landmark, thereby deserving a definite article in its name.</p>
                            </div>
                        </div>
                    </div>
