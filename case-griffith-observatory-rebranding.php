                            <div class="introduction__wrap"><h2><strong>Art Deco<br>X<br>Minimalism</strong></h2></div>
                            <div class="introduction__wrap">
                                <div class="section-texts">
                                    <p>The combination of Art Deco and Minimalism in The Griffith Observatory's
                                    new identity creates a compelling and cohesive visual representation. Drawing inspiration from the Art Deco movement, known for its elegance, geometric shapes, and lavish details, infuses the brand with a sense of sophistication and timeless charm.</p>
                                    <p>The incorporation of minimalism further enhances the brand's clarity, allowing
                                    essential elements to shine through while eliminating unnecessary distractions. 
                                    By uniting these styles, the new identity captures the observatory's rich heritage,
                                    while embracing modernity, elegance, and a visual language that conveys a sense 
                                    of awe-inspiring exploration and scientific advancement.</p>
                                </div>
